% --- Executes on button press in upload_picture.
    function upload_picture_Callback(hObject, eventdata, handles)
    % hObject    handle to upload_picture (see GCBO)
    % eventdata  reserved - to be defined in a future version of MATLAB
    % handles    structure with handles and user data (see GUIDATA)

    % Use uigetfile to get the image file
    [filename, pathname] = uigetfile({'*.jpg;*.png;*.bmp', 'Image Files (*.jpg, *.png, *.bmp)'; '*.*', 'All Files (*.*)'}, 'Select Image File');
    
    % Check if the user canceled the file selection
    if isequal(filename, 0)
        return; % Exit the function if canceled
    end
    
    % Create the full file path
    fullFilePath = fullfile(pathname, filename);
    
    % Read the image
    img = imread(fullFilePath);
    
    % Display the image in axes1
    axes(handles.axes1);
    imshow(img);
    
    % Store the image and filename in app data
    setappdata(0, 'img', img);
    setappdata(0, 'filename', fullFilePath);




